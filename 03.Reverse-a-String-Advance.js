function reverseString(inputString) {
    return inputString.split('').reverse('').join('');
}

string1 = reverseString('Hello World');
string2 = reverseString('Hello Federex');
string3 = reverseString('Greetings to the world!');

console.log(string1);
console.log(string2);
console.log(string3);



